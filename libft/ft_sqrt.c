#include "libft.h"

int		ft_sqrt(int nb)
{
	int n;
	int a;

	n = 1;
	a = 0;
	while (a != nb && a <= nb)
	{
		a = n * n;
		n++;
	}
	if (a == nb)
		return (n - 1);
	return (0);
}
