#include "libft.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
	{
		ft_putstr("-214748364");
		n = 8;
	}
	if (n < 0 && n > -2147483648)
	{
		n *= -1;
		ft_putchar('-');
	}
	if (n > 9 && n <= 2147483647 && n > -2147483648)
	{
		ft_putnbr(n / 10);
		n %= 10;
	}
	if (n < 10)
		ft_putchar(n + 48);
}
