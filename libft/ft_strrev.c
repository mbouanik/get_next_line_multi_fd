#include "libft.h"

char	*ft_strrev(char *str)
{
	char	tmp;
	size_t	j;
	size_t	i;

	i = 0;
	j = ft_strlen(str) - 1;
	while (i <= j)
	{
		tmp = str[i];
		str[i] = str[j];
		str[j] = tmp;
		j--;
		i++;
	}
	return (str);
}
